    F_EMAIL VARCHAR2(100), /* 찜한사람 */
    P_EMAIL VARCHAR2(100), /* 찜당한사람 */
    FOREIGN KEY(F_EMAIL) REFERENCES FINDERMEMBERS(F_EMAIL),
